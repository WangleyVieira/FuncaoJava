package funcaocalculadoraprincipal;

/**
 *
 * @author Msv Wangley
 */
public class Funcao {
    public static double soma(double n1, double n2){
        double resultSoma = n1 + n2;
        return resultSoma;
    }
    
    public static double subtracao(double n1, double n2){
        double resultSubtracao = n1 - n2;
        return resultSubtracao;
    }
    
    public static double divisao(double n1, double n2){
        double resultDivisao = n1 / n2;
        return resultDivisao;
    }
    
    public static double multiplicacao(double n1, double n2){
        double resultMultiplicacao = n1 * n2;
        return resultMultiplicacao;
    }
    
}
